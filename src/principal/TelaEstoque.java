    private void atualizaTabela() throws SQLException, ClassNotFoundException
    {
        JTB_dados.setModel(dtm_tabela);
        dtm_tabela.setNumRows(0);
        listaEstoque = EstoqueDAO.buscarTodosProdutos();
        for (Estoque e : listaEstoque) 
        {
            dtm_tabela.addRow(new Object[]{e.getCodigo(), e.getProduto(), e.getValor(), e.getQtdeProduto()});
	}
    }

